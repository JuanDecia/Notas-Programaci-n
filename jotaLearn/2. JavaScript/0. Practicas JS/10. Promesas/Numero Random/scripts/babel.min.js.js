const promise = new Promise ((resolve, reject) => {
    setTimeout ( () => {
        const array = new Uint32Array(1);
        crypto.getRandomValues(array);

        const number = array[0] % 10;
        if (number % 2 === 0) {
            resolve (number)
        }
        else {
            reject('Rechazado, no es un nÃºmero par.')
        }
    }, 1000);
});

async function procesar() {

    try {
        let resultado = await promise;
        console.log(`Resultado: ${resultado}`)   
        return resultado;
    }

    catch(error) {
        console.log(error);
        return error;
    }
}

const button = document.querySelector('#boton-promesa');
const resultadoPromesa = document.querySelector('.resultado-promesa');

button.addEventListener('click', function() {

    procesar().then(resultado => {
        resultadoPromesa.innerHTML = resultado;
    });
});